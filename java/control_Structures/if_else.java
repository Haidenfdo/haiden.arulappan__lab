package control_Structures;

public class if_else {

    public static void main(String[] args) {

        int time = 20;
        if (time < 18) {
            System.out.println("Good morning");
        } else if (time > 22) {
            System.out.println("Good evening");
        } else {
            System.out.println("Good night");
        }
    }

}
