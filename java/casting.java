public class casting {
    public static void main(String[] args) {

        // Widening casting
        int mynum = 8;
        double mydouble = mynum;

        System.out.println(mynum);
        System.out.println(mydouble);

        // Narrow casting

        double mydoub = 9.75;
        int myint = (int) mydoub;

        System.out.println(mydoub);
        System.out.println(myint);

    }
}
